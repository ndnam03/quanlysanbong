    private void loadData(List<HoaDon> list) {
        tm.setRowCount(0);
        list.forEach(o -> {
            tm.addRow(new Object[]{
                o.getId(), o.getIdKH(), o.getTongTien(), o.getNgayTao(), o.getTichDiem()
            });
        });
        soLuong = list.size();
        jLabel3.setText("Tổng số lượng đơn: " + " " + list.size());
    }

