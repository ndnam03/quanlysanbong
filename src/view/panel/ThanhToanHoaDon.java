    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        if(row < 0){
            JOptionPane.showMessageDialog(this, "Bạn phải chọn đối tượng để thanh toán!!!");
            return;
        }
        
        String customerName = jTextField1.getText();
        String customerPhone = jTextField10.getText();
        String soGio = jTextField12.getText();
        String loaiSan = jTextField13.getText();
        String diaChi = jTextField14.getText();
        String tongTien = jTextField11.getText();

        SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        Date ngayThanhToan = new Date();
        Random random = new Random();
        int randomNumber = random.nextInt(100000);
        String randomString = "HD" + randomNumber;

        // Tạo tệp PDF mới
        com.itextpdf.text.Document document = new com.itextpdf.text.Document(PageSize.A4, 50, 50, 50, 50);
        try {
            // Tạo tệp PDF
            PdfWriter.getInstance(document, new FileOutputStream("H:\\QuanLyDuAn\\hoadonchitiet.pdf"));
            document.open();

            // Tạo tiêu đề của hóa đơn
            Paragraph title = new Paragraph("HOA DON THANH TOAN", FontFactory.getFont(FontFactory.HELVETICA_BOLD, 16));
            title.setAlignment(Element.ALIGN_CENTER);
            document.add(title);

            // Tạo bảng chứa thông tin của khách hàng và hóa đơn
            PdfPTable table = new PdfPTable(2);
            table.setWidthPercentage(100);
            table.setSpacingBefore(20);
            table.setSpacingAfter(20);

            // Thêm thông tin của khách hàng vào bảng
            PdfPCell cell = new PdfPCell(new Paragraph("Thông tin khách hàng", FontFactory.getFont(FontFactory.HELVETICA_BOLD, 12)));
            cell.setColspan(2);
            cell.setHorizontalAlignment(Element.ALIGN_CENTER);
            cell.setBackgroundColor(new Font().getColor());
            table.addCell(cell);

            table.addCell("Tên khách hàng:");
            table.addCell(customerName);

            table.addCell("Phone:");
            table.addCell(customerPhone);

            table.addCell("Address:");
            table.addCell(diaChi);

            // Thêm thông tin của hóa đơn vào bảng
            cell = new PdfPCell(new Paragraph("Thong Tin Hoa Don", FontFactory.getFont(FontFactory.HELVETICA_BOLD, 12)));
            cell.setColspan(2);
            cell.setHorizontalAlignment(Element.ALIGN_CENTER);
            cell.setBackgroundColor(new Font().getColor());
            table.addCell(cell);

            table.addCell("So Hoa Don:");
            table.addCell(randomString);

            table.addCell("Loai San:");
            table.addCell(loaiSan);

            table.addCell("So Gio:");
            table.addCell(String.valueOf(soGio));

            table.addCell("Ngay Thue:");
            table.addCell(String.valueOf(tm.getValueAt(0, 1)));

            table.addCell("Ngày thanh toán:");
            table.addCell(dateFormat.format(ngayThanhToan));

            table.addCell("Tong Tien:");
            table.addCell(String.valueOf(tongTien));

            document.add(table);

            Paragraph thankYou = new Paragraph("Con On Quy Khach Rat Nhieu!", FontFactory.getFont(FontFactory.HELVETICA, 12));
            thankYou.setAlignment(Element.ALIGN_CENTER);
            document.add(thankYou);
            // Đóng tệp PDF
            document.close();
            
            DatSan dangKi = datSanService.getAll().get(row);
            KhachHang khachHang = khachHangService.getOne(dangKi.getIdKhachHang());
            HoaDon hoaDon = HoaDon.builder()
                    .idKH(khachHang.getId())
                    .tongTien(Double.parseDouble(tongTien))
                    .ngayTao(ngayThanhToan)
                    .tichDiem(100)
                    .build();
            boolean f = hoaDonService.add(hoaDon);
           datSanService.update(null, dangKi.getId());
            if (f) {
                JOptionPane.showMessageDialog(this, "Thanh Toán Thành Công");
            }

            if (Desktop.isDesktopSupported()) {
                try {
                    Desktop.getDesktop().open(new File("H:\\java3\\hoadonchitiet.pdf"));
                } catch (IOException e) {
                    e.printStackTrace();
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton3ActionPerformed


