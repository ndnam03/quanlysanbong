    private void loadKhachHang(List<DatSan> list) {
        tm.setRowCount(0);
        list.forEach(o -> {
            tm.addRow(new Object[]{
                o.getId(), o.getNgayDat(), o.getSoGio(),
                o.getSanBongId(), o.getIdNhanVien(),
                o.getSotienthanhtoan(), o.getTongTien() - o.getSotienthanhtoan(),
                o.getTongTien()
            });
        });

    }

