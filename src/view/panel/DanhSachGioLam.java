    void loadData(List<ChamCong> list) {

        tm.setRowCount(0);
        list.forEach(o -> {
            String name = accountService.getOne(o.getIdNhanVien()).getName();
            Time thoiGianBatDau = o.getGioBatDau();
            Time thoiGianKetThuc = o.getGioKetThuc();

            // Chuyển đổi thời gian về đơn vị giờ và phút
            long batDauMillis = thoiGianBatDau.getTime();
            long ketThucMillis = thoiGianKetThuc.getTime();
            long thoiGianLamViecMillis = ketThucMillis - batDauMillis;

            // Chuyển đổi thời gian làm việc từ millis sang phút
            long thoiGianLamViecPhut = TimeUnit.MILLISECONDS.toMinutes(thoiGianLamViecMillis);

            // Giá tiền mỗi giờ
            int giaTienMoiGio = 20000;

            // Tính tổng tiền
            int tongTien = (int) (thoiGianLamViecPhut * giaTienMoiGio / 60);

            tm.addRow(new Object[]{
                o.getId(), name, o.getNgayLam(), thoiGianBatDau, thoiGianKetThuc, tongTien
            });

        });

    }

    int calculateTotalSalary(ChamCong chamCong) {
        Time thoiGianBatDau = chamCong.getGioBatDau();
        Time thoiGianKetThuc = chamCong.getGioKetThuc();
        long batDauMillis = thoiGianBatDau.getTime();
        long ketThucMillis = thoiGianKetThuc.getTime();
        long thoiGianLamViecMillis = ketThucMillis - batDauMillis;
        long thoiGianLamViecPhut = TimeUnit.MILLISECONDS.toMinutes(thoiGianLamViecMillis);
        int giaTienMoiGio = 20000;
        return (int) (thoiGianLamViecPhut * giaTienMoiGio / 60);
    }

