        int row = jTable4.getSelectedRow();

        // Kiểm tra xem người dùng có click vào hàng hợp lệ không
        if (row >= 0 && row < chamCongService.getAll().size()) {
            ChamCong chamCong = chamCongService.getAll().get(row);
            String name = accountService.getOne(chamCong.getIdNhanVien()).getName();

            // Tính tổng lương của nhân viên và hiển thị lên JTextField
            int totalSalary = calculateTotalSalary(chamCong);
            jTextField1.setText(name);
            jTextField2.setText(totalSalary + " VNĐ");
        }
